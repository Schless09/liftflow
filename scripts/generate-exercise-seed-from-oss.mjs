#!/usr/bin/env node
/**
 * Fetches the full ExerciseDB OSS v1 catalog and writes supabase/seed_exercises_oss.sql
 *
 * Pagination uses `?after={exerciseId}` (the `cursor` param loops on this API).
 *
 * Docs: https://oss.exercisedb.dev/docs
 * Terms: OSS tier is non-commercial; attribute https://ascendapi.com/ when required.
 *
 * Usage: node scripts/generate-exercise-seed-from-oss.mjs
 */

import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.join(__dirname, "..", "supabase", "seed_exercises_oss.sql");
const OSS_BASE = "https://oss.exercisedb.dev/api/v1/exercises";

function titleCase(name) {
  return name
    .trim()
    .split(/(\s+)/)
    .map((part) => (/\s+/.test(part) ? part : part.charAt(0).toUpperCase() + part.slice(1).toLowerCase()))
    .join("");
}

function sqlStr(s) {
  if (s == null) return "NULL";
  return `'${String(s).replace(/'/g, "''")}'`;
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function fetchAllExercises() {
  /** @type {Map<string, unknown>} */
  const byId = new Map();
  let after = undefined;
  const maxPages = 70;
  const limit = 100;
  const delayMs = 450;

  for (let page = 0; page < maxPages; page++) {
    const url = new URL(OSS_BASE);
    url.searchParams.set("limit", String(limit));
    if (after) url.searchParams.set("after", after);

    let res = /** @type {Response | undefined} */ (undefined);
    let attempt = 0;
    const maxAttempts = 8;
    while (attempt < maxAttempts) {
      res = await fetch(url, { headers: { Accept: "application/json" } });
      if (res.status === 429) {
        const ra = res.headers.get("retry-after");
        const retryMs = ra ? Math.max(5000, Number(ra) * 1000) : 35_000;
        console.error(`Rate limited (page ${page + 1}); waiting ${Math.round(retryMs / 1000)}s…`);
        await sleep(retryMs);
        attempt++;
        continue;
      }
      if (!res.ok) {
        throw new Error(`OSS HTTP ${res.status}: ${await res.text()}`);
      }
      break;
    }
    if (!res?.ok) throw new Error("Too many rate-limit (429) responses from OSS API");

    const json = await res.json();
    const rows = Array.isArray(json.data) ? json.data : [];
    if (rows.length === 0) break;

    for (const row of rows) {
      if (!row || typeof row !== "object") continue;
      const o = row;
      const id = o.exerciseId;
      if (typeof id === "string") byId.set(id, row);
    }

    const total = typeof json.meta?.total === "number" ? json.meta.total : null;
    if (total != null && byId.size >= total) break;

    const last = rows[rows.length - 1];
    const lastId = last && typeof last === "object" && "exerciseId" in last && typeof last.exerciseId === "string" ? last.exerciseId : null;
    if (!lastId || lastId === after) break;
    after = lastId;
    await sleep(delayMs);
  }

  return [...byId.values()];
}

function normalizeRow(row) {
  if (!row || typeof row !== "object") return null;
  const o = row;
  const exerciseId = o.exerciseId;
  const name = o.name;
  const gifUrl = o.gifUrl;
  const bodyParts = o.bodyParts;
  const equipments = o.equipments;
  if (typeof exerciseId !== "string" || typeof name !== "string" || typeof gifUrl !== "string") return null;

  const muscle =
    Array.isArray(bodyParts) && bodyParts.length > 0 && typeof bodyParts[0] === "string"
      ? bodyParts[0].toLowerCase()
      : "general";
  const equipment =
    Array.isArray(equipments) && equipments.length > 0 && typeof equipments[0] === "string"
      ? equipments[0].toLowerCase()
      : "other";

  return {
    exercisedb_oss_id: exerciseId,
    canonical_name: titleCase(name),
    muscle_group: muscle,
    equipment,
    gif_url: gifUrl,
  };
}

async function main() {
  console.error("Fetching exercises from oss.exercisedb.dev …");
  const raw = await fetchAllExercises();
  const rows = raw.map(normalizeRow).filter(Boolean);

  /** Avoid violating unique(canonical_name) if OSS titles collide after Title Case. */
  const canonCount = new Map();
  for (const r of rows) {
    canonCount.set(r.canonical_name, (canonCount.get(r.canonical_name) ?? 0) + 1);
  }
  for (const r of rows) {
    if ((canonCount.get(r.canonical_name) ?? 0) > 1) {
      r.canonical_name = `${r.canonical_name} · ${r.exercisedb_oss_id}`;
    }
  }

  if (rows.length < 500) {
    throw new Error(`Expected ~1500+ unique OSS exercises, got ${rows.length}`);
  }

  const lines = [
    "-- Auto-generated by scripts/generate-exercise-seed-from-oss.mjs",
    "-- Source: https://oss.exercisedb.dev/docs (ExerciseDB OSS v1)",
    "-- Re-run after API changes: npm run generate:exercise-seed",
    "",
    "truncate table public.exercises restart identity cascade;",
    "",
    "insert into public.exercises (canonical_name, aliases, muscle_group, equipment, default_rest_seconds, gif_url, exercisedb_oss_id)",
    "values",
  ];

  const valueLines = rows.map((r, i) => {
    const tail = i === rows.length - 1 ? "" : ",";
    return (
      `  (${sqlStr(r.canonical_name)}, '{}', ${sqlStr(r.muscle_group)}, ${sqlStr(r.equipment)}, 75, ` +
      `${sqlStr(r.gif_url)}, ${sqlStr(r.exercisedb_oss_id)})${tail}`
    );
  });

  lines.push(...valueLines);
  lines.push("on conflict (exercisedb_oss_id) do update set");
  lines.push("  canonical_name = excluded.canonical_name,");
  lines.push("  muscle_group = excluded.muscle_group,");
  lines.push("  equipment = excluded.equipment,");
  lines.push("  gif_url = excluded.gif_url;");
  lines.push("");

  writeFileSync(OUT, lines.join("\n"), "utf8");
  console.error(`Wrote ${rows.length} exercises to ${OUT}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
